function App() {
  return (
    <div className="container">
      <Header />
      <Menu />
      <Footer />
    </div>
  );
}

function Header() {
  return (
    <header className="header">
      <h1>Welcome to Pizza World!</h1>
    </header>
  );
}

function Menu() {
  const pizzas = pizzaData;
  // const pizzas = {};
  const numberOfPizzas = pizzas.length;
  return (
    <main className="menu">
      <h2>OUR MENU</h2>
      {/*if number of pizzas is 0 then we don't display anything*/}
      {numberOfPizzas > 0 ? (
        //<> </> id used when we want to have no root element in JSX
        //we can see JSX always needs one root element
        //but in our code below we have <p> and <ul> elements
        //this is called as fragmentation
        <>
          <p>
            Taste the authentic Italian Pizzas straight from our ovens to your
            home!!
          </p>
          <ul className="pizzas">
            {pizzaData.map((pizza) => (
              //passing props pizzaObj and key to function Pizza
              <Pizza pizzaObj={pizza} key={pizza.name} />
            ))}
          </ul>
        </>
      ) : (
        <p>We're still working on our menu. Please come back later :)</p>
      )}
    </main>
  );
}

//instead of passing props, using destructuring to get object
function Pizza({ pizzaObj }) {
  console.log(pizzaObj);
  //if (pizzaObj.soldOut) return null;
  return (
    <li className={`pizza ${pizzaObj.soldOut ? "sold-out" : ""}`}>
      <img src={pizzaObj.photoName} alt={pizzaObj.name}></img>
      <div>
        <h3>{pizzaObj.name}</h3>
        <p>{pizzaObj.ingredients}</p>
        <span>{pizzaObj.soldOut ? "SOLD OUT" : pizzaObj.price}</span>
      </div>
    </li>
  );
}

function Footer() {
  const hour = new Date().getHours();
  //24 hour format
  const openHour = 12;
  const closeHour = 22;
  const isOpen = hour >= openHour && hour <= closeHour;
  console.log(isOpen);
  return (
    <footer className="footer">
      {isOpen ? (
        <Order closeHour={closeHour} />
      ) : (
        <p>
          We're happy to open between {openHour}:00 and {closeHour}:00
        </p>
      )}
    </footer>
  );
}

function Order({ closeHour }) {
  return (
    <div className="order">
      <p>We are open until {closeHour}:00</p>
      <button className="btn">ORDER</button>
    </div>
  );
}
const root = ReactDOM.createRoot(document.getElementById("root"));
root.render(
  <StrictMode>
    <App />
  </StrictMode>
);
